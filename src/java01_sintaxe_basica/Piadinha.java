package java01_sintaxe_basica;

public class Piadinha {

    public static void main(String[] args) {

        // Número Octagonal
        int Oct31 = 031;
        System.out.println(Oct31); // Resultado 25

        // Número Decimal
        int Dec25 = 25;
        System.out.println(Dec25);

        System.out.println(Oct31 == Dec25); // Verdadeiro

    }

}
